fun main()
{
    // Arithmetic Operators
    val x = 50
    val y = 100
    println(x+y)
    println(x-y)
    println(x*y)
    println(x/y)
    println(x%y)

    // Comparison Operator
    var box1 = 67
    var box2 = 60


    println(box1 == box2)
    println(box1 != box2)
    println(box1 >= box2)
    println(box1 <= box2)
    println(box1 > box2)
    println(box1 < box2)

    // logical Operator
    var X = true
    var Y = false

    if(X && Y)
        println("true")
    else
        println(false)

    if(X || Y)
        println(true)
    else
        println(false)

    if(!Y)
        println("I am a Developer")

}